package util.jterm;

import java.awt.event.KeyListener;
import java.awt.event.MouseListener;
import java.awt.Font;

//////////////////////////////////////////////////////////////
/**
 * This is the object used to handle textual information for
 * reading and writing. Actually all operations are implemented in the
 * standard text areas java.awt.TextArea and javax.swing.JTextArea.
 * This "generalization" allows to take advantage of both possibilities
 * as well as other implementations.
 *
 * @see util.jconsole.taimp.AWTTextArea
 * @see util.jconsole.taimp.SwingTextArea
 *
 * @author Carlos Rueda
 * @version 0.1 2000-01-13
 * @version 0.2 2000-01-17
 */
public interface ITextArea
{
	//////////////////////////////////////////////////////////////
	public void addKeyListener(KeyListener l);
	//////////////////////////////////////////////////////////////
	public void addMouseListener(MouseListener l);
	//////////////////////////////////////////////////////////////
	public void append(String s);
	//////////////////////////////////////////////////////////////
	public void copy();
	//////////////////////////////////////////////////////////////
	public void cut();
	//////////////////////////////////////////////////////////////
	public int getCaretPosition();
	//////////////////////////////////////////////////////////////
	public Font getFont();
	//////////////////////////////////////////////////////////////
	public String getText();
	//////////////////////////////////////////////////////////////
	public void insert(String s, int pos);
	//////////////////////////////////////////////////////////////
	public boolean isEditable();
	//////////////////////////////////////////////////////////////
	public void paste();
	//////////////////////////////////////////////////////////////
	public void removeKeyListener(KeyListener l);
	//////////////////////////////////////////////////////////////
	public void removeMouseListener(MouseListener l);
	//////////////////////////////////////////////////////////////
	public void replaceRange(String str, int start, int end);
	//////////////////////////////////////////////////////////////
	public void requestFocus();
	//////////////////////////////////////////////////////////////
	public void setCaretPosition(int pos);
	//////////////////////////////////////////////////////////////
	public void setEditable(boolean b);
	//////////////////////////////////////////////////////////////
	public void setFont(Font f);
	//////////////////////////////////////////////////////////////
	public void setText(String t);
}