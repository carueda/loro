package loro.tipo;

import loro.Loro.Str;

///////////////////////////////////////////////////////////
/**
 * Tipo real
 */
class TipoReal extends TipoBasico
{
	//////////////////////////////////////////////////////////////////////
	public boolean esAsignable(Tipo t)
	{
		return t.esNumerico();
	}
	
	//////////////////////////////////////////////////////////////////////
	public boolean esConvertibleA(Tipo t)
	{
		return t.esCadena() 
			|| t.esEntero()
			|| t.esReal()
		;
	}
	
	//////////////////////////////////////////////////////////////////////
	public boolean esNumerico()
	{
		return true;
	}
	
	//////////////////////////////////////////////////////////////////////
	public boolean esReal()
	{
		return true;
	}
	
	//////////////////////////////////////////////////////////////////////
	public boolean esSumable()
	{
		return true;
	}
	

	
	//////////////////////////////////////////////////////////////////////
	public Object obtValorDefecto()
	{
		return defectoReal;
	}
	
	//////////////////////////////////////////////////////////////////////
	public String toString()
	{
		return Str.get("real");
	}

	/** Valor por defecto para un real Loro. */
	private static final Double defectoReal = new Double(0);
	/** Mi unica instancia */
	private static final Tipo instancia = new TipoReal();

	//////////////////////////////////////////////////////////////////////
	private TipoReal() {}

	//////////////////////////////////////////////////////////////////////
	static Tipo obtInstancia()
	{
		return instancia;
	}

	//////////////////////////////////////////////////////////////////////
	private Object readResolve()
	throws java.io.ObjectStreamException
	{
		return instancia;
	}
}